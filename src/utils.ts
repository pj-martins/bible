export const cleanText = (input: string) => {
	let replaced = input;
	for (let dc of decodeMap) {
		let regex = new RegExp(dc.orig, 'g');
		replaced = replaced.replace(regex, dc.nw);
	}
	return replaced.replace(/<[^>]*>/g, '').trim();
}

